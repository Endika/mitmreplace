#ifndef __PATTERNS_H__
# define __PATTERNS_H__

#define MATCH_IS_BINARY     1
#define REPLACE_IS_BINARY   2

typedef struct _pattern_
{
    struct _pattern_ *next;

    unsigned char   *match;
    unsigned char   *replace;
    unsigned char   flags;
    size_t          mlen;
    size_t          rlen;
    char			*alias;
} pattern_t , *ppattern_t;

static const char patterns_error[4][21] =
{
    "Document syntax error",
    "Empty document",
    "Root node error",
    "No patterns loaded",
};
#define GET_PATTERNS_ERRSTR(val) (val<1||val>4)?NULL:patterns_error[val-1]

void free_patterns ();
unsigned short load_patterns ( void *p );

#endif /* __PATTERNS_H__ */
